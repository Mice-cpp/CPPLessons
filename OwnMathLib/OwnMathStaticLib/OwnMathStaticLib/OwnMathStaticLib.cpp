#include "pch.h"
#include "framework.h"

// Definitions of Body Functions
int addInt(int a, int b)
{
    return a + b;
}

int subInt(int a, int b)
{
    return a - b;
}

long int mulInt(int a, int b)
{
    return a * b;
}

double divInt(int a, int b)
{
    return (double)(a / b);
}
